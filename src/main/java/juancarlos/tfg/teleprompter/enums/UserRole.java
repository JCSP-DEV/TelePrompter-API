package juancarlos.tfg.teleprompter.enums;

/**
 * Enumeration representing user roles in the system.
 * Defines the available roles that can be assigned to users.
 *
 * @author Juan Carlos
 */
public enum UserRole {
    /**
     * Administrator role with full system access.
     */
    ADMIN,

    /**
     * Regular user role with limited system access.
     */
    USER
}
